                            <div class="question__header"><i class="fa fa-angle-down"></i> <span
                                    class="question__title">
                                    What is a Dedicated Server and how does it work?
                                </span></div>
                        </header>
                        <div style="display:none;">
                            <div class="question__answer">A Dedicated Server is a physical server where all the
                                resources of one machine are dedicated to a single user. Unlike a Shared Hosting server,
                                where resources are distributed among the respective users, Dedicated Servers for
                                hosting let you benefit from every resource available such as RAM, storage, computing
                                power, etc.</div>
                        </div>

                    <div class="question">
                        <header>
                            <div class="question__header"><i class="fa fa-angle-down"></i> <span
                                    class="question__title">
                                    What is Reseller Hosting?
                                </span></div>
                        </header>
                        <div style="display:none;">
                            <div class="question__answer">Reseller Hosting is a type of web hosting that allows you (the
                                reseller) to purchase a hosting provider’s resources wholesale and resell them to your
                                clients for a profit. Ideal for web developers, web design firms or web hosting
                                entrepreneurs, Reseller Hosting allows you to create your own branded hosting service
                                and your own pricing structure.</div>
                        </div>
                        <footer></footer>
                    </div>
                    <div class="question">
                        <header>
                            <div class="question__header"><i class="fa fa-angle-down"></i> <span
                                    class="question__title">
                                    How do I become a hosting reseller with Namecheap?
                                </span></div>
                        </header>
                        <div style="display:none;">
                            <div class="question__answer">It’s easy. Simply follow these steps:
                                <ol>
                                    <li>Select your desired Reseller Hosting plan (Nebula, Galaxy Expert, Universe Pro).
                                    </li>
                                    <li>In order to set up your reseller storefront, create your website using the CMS
                                        of your choice.</li>
                                    <li>Decide which type of hosting (Shared, Dedicated, VPS) you’d like to sell to your
                                        clients.</li>
                                    <li><a href="/support/knowledgebase/article/9391/27/reseller-hosting-getting-started/#install"
                                            target="_blank">Set up your WHMCS billing platform</a> to start automating
                                        your sales.</li>
                                    <li>Finish by attracting potential clients to your website in order to drive sales.
                                    </li>
                                </ol>
                            </div>
                        </div>
                        <footer></footer>
                    </div>
                    <div class="question">
                        <header>
                            <div class="question__header"><i class="fa fa-angle-down"></i> <span
                                    class="question__title">
                                    How does Reseller Hosting work?
                                </span></div>
                        </header>
                        <div style="display:none;">
                            <div class="question__answer">When you select your desired plan, you are provided with
                                access via cPanel to your Web Host Manager (WHM) program. This is where you can create
                                and manage all your reseller accounts on the server, which other resellers might occupy.
                                (Keep in mind all created accounts are referred to as “resold accounts.”) Your reseller
                                privileges are determined by your hosting provider, i.e. Namecheap.</div>
                        </div>
                        <footer></footer>
                    </div>
                    <div class="question">
                        <header>
                            <div class="question__header"><i class="fa fa-angle-down"></i> <span
                                    class="question__title">
                                    What is the difference between Reseller Hosting and Shared Hosting?
                                </span></div>
                        </header>
                        <div style="display:none;">
                            <div class="question__answer">In essence, Reseller Hosting gives you more control and more
                                resources than Shared Hosting. Think of Reseller Hosting as a parental figure to Shared
                                Hosting. As a reseller, you’re allowed to create and manage multiple Shared Hosting
                                accounts, with the intention of selling them to your clients.</div>
                        </div>
                        <footer></footer>
                    </div>
                    <div class="question">
                        <header>
                            <div class="question__header"><i class="fa fa-angle-down"></i> <span
                                    class="question__title">
                                    What are WHM and WHMCS?
                                </span></div>
                        </header>
                        <div style="display:none;">
                            <div class="question__answer">Although they sound similar, Web Host Manager (WHM) and Web
                                Host Manager Complete Solution (WHMCS) are completely different. While WHM is a handy
                                program that gives you administrative access to the back end of cPanel, WHMCS automates
                                your billing and support services. If you plan to become a reseller, you need WHMCS to
                                automate the entire process.</div>
                        </div>
                        <footer></footer>
                    </div>
